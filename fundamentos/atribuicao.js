//Atribuição é um tipo de operador. Existe vários operadores.
// = É O SINAL DA ATRIBUIÇÃO
const a = 7
let b = 3

//Atribuição aditiva
b += a  // b = b + a
console.log(b)

//Atribuição substrativa
b -= 4  //B = B - 4 
console.log(b)


//Atribuição multiplicativa
b *= 2 //B = B * 2
console.log(b)

//Atribuição divisão
b /=2 // b = b /2
console.log(b)

//Atribuição Modular
b %= 2 // B = B %
console.log(b)